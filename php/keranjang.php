  function updateTotal() {
    var checkboxes = document.querySelectorAll('.checkbox-container input[type="checkbox"]');
    var totalHarga = 0;
    var totalJumlah = 0;

    checkboxes.forEach(function (checkbox) {
      if (checkbox.checked) {
        var containerIsi = checkbox.closest('.container-isi');
        var subtotal = containerIsi.querySelector('.subtotal').textContent;
        var harga = parseInt(subtotal.replace(/[^0-9]/g, ''));
        totalHarga += harga;

        var quantityInput = containerIsi.querySelector('.quantity-input');
        var jumlah = parseInt(quantityInput.value);
        totalJumlah += jumlah;
      }
    });

    document.getElementById('total-harga').textContent = 'Total Harga: Rp. ' + totalHarga.toLocaleString();
    document.getElementById('total-jumlah').textContent = 'Total Jumlah: ' + totalJumlah;
  }

  function checkout() {
    var checkboxes = document.querySelectorAll('.checkbox-container input[type="checkbox"]');
    var selectedItems = [];

    checkboxes.forEach(function (checkbox) {
      if (checkbox.checked) {
        var containerIsi = checkbox.closest('.container-isi');
        var judul = containerIsi.previousElementSibling.querySelector('h3').textContent;
        var harga = containerIsi.querySelector('.harga').textContent;
        var jumlah = containerIsi.querySelector('.quantity-input').value;
        var subtotal = containerIsi.querySelector('.subtotal').textContent;
        var gambar = containerIsi.querySelector('img').src;

        selectedItems.push({
          judul: judul,
          harga: harga,
          jumlah: jumlah,
          subtotal: subtotal,
          gambar: gambar
        });
      }
    });

    // Redirect to checkout.php with selected items as parameter
    var totalHarga = document.getElementById('total-harga').textContent;
    var harga = parseInt(totalHarga.replace(/[^0-9]/g, ''));
    var queryString = "?total=" + harga;

    if (selectedItems.length > 0) {
      queryString += "&items=" + encodeURIComponent(JSON.stringify(selectedItems));
    }

    window.location.href = "../php/invoice.php" + queryString;
  }

  function formatNumber(number) {
    return number.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ".");
  }

  var minusBtns = document.querySelectorAll('.minus-btn');
  var plusBtns = document.querySelectorAll('.plus-btn');
  var quantityInputs = document.querySelectorAll('.quantity-input');
  var subtotalElements = document.querySelectorAll('.subtotal');

  for (var i = 0; i < minusBtns.length; i++) {
    minusBtns[i].addEventListener('click', function () {
      var currentContainer = this.parentNode;
      var quantityInput = currentContainer.querySelector('.quantity-input');
      var subtotalElement = currentContainer.nextElementSibling.querySelector('.subtotal');
      var currentValue = parseInt(quantityInput.value);

      if (currentValue > 1) {
        quantityInput.value = currentValue - 1;
        var harga = parseInt(currentContainer.previousElementSibling.querySelector('.harga').textContent.replace('Rp. ', '').replace('.', ''));
        var subtotal = (currentValue - 1) * harga;
        subtotalElement.textContent = 'Rp. ' + formatNumber(subtotal);
      }
    });
  }

  for (var i = 0; i < plusBtns.length; i++) {
    plusBtns[i].addEventListener('click', function () {
      var currentContainer = this.parentNode;
      var quantityInput = currentContainer.querySelector('.quantity-input');
      var subtotalElement = currentContainer.nextElementSibling.querySelector('.subtotal');
      var currentValue = parseInt(quantityInput.value);

      quantityInput.value = currentValue + 1;
      var harga = parseInt(currentContainer.previousElementSibling.querySelector('.harga').textContent.replace('Rp. ', '').replace('.', ''));
      var subtotal = (currentValue + 1) * harga;
      subtotalElement.textContent = 'Rp. ' + formatNumber(subtotal);
    });
  }
